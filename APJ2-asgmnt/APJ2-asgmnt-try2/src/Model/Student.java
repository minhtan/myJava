package Model;

/**
 *
 * @author Tan
 */
public class Student {
    private int id;
    private String name;
    private String address;
    private String telNo;
    private String dob;
    private int sex;

    public Student(int id, String name, String address, String telNo, String dob, int sex) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.telNo = telNo;
        this.dob = dob;
        this.sex = sex;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTelNo() {
        return telNo;
    }

    public void setTelNo(String telNo) {
        this.telNo = telNo;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public int getSex() {
        return sex;
    }

    public void setSex(int sex) {
        this.sex = sex;
    }
    
}
